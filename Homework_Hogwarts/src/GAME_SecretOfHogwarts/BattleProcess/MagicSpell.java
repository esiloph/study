package GAME_SecretOfHogwarts.BattleProcess;

public class MagicSpell {
    public String 이름;
    public int 데미지;
    public int 마력소모;
    public String 시전;
    public MagicSpell(String 이름, int 데미지, int 마력소모, String 시전) {
        this.이름=이름;
        this.데미지=데미지;
        this.마력소모=마력소모;
        this.시전=시전;
    }
}
